package introduccion;

public class BuclesForSencillo {

	public static void main(String[] args) {
		
		
		//Este bucle for determinado nos muestra por pantalla 10 veces "Hola mundo" 
		
		for (int i = 0; i < 10; i++) {
			
			System.out.println("Hola mundo");
		}
		
		System.out.println("Fin del bucle");
		
	}

}


